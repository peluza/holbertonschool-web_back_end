#!/usr/bin/env python3
"""5-sum_list.py"""

import typing


def sum_list(input_list: typing.List[typing.Union[float]]) -> float:
    """sum_mixed_list

    Args:
        mxd_lst (typing.List[typing.Union[float]]): floats as argument

    Returns:
        float: their sum as a float.
    """
    return sum(input_list)
